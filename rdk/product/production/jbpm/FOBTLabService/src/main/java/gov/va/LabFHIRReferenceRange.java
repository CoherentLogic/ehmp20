package gov.va;

public class LabFHIRReferenceRange {

	private LabFHIRHigh high;
	private LabFHIRLow low;

	/**
	* 
	* @return
	* The high
	*/
	public LabFHIRHigh getHigh() {
		return high;
	}

	/**
	* 
	* @param high
	* The high
	*/
	public void setHigh(LabFHIRHigh high) {
		this.high = high;
	}

	/**
	* 
	* @return
	* The low
	*/
	public LabFHIRLow getLow() {
		return low;
	}

	/**
	* 
	* @param low
	* The low
	*/
	public void setLow(LabFHIRLow low) {
		this.low = low;
	}
	
}
