package gov.va;

public class LabFHIRCoding {

	private String system;
	private String code;
	private String display;

	/**
	* 
	* @return
	* The system
	*/
	public String getSystem() {
		return system;
	}
	
	/**
	* 
	* @param system
	* The system
	*/
	public void setSystem(String system) {
		this.system = system;
	}
	
	/**
	* 
	* @return
	* The code
	*/
	public String getCode() {
		return code;
	}
	
	/**
	* 
	* @param code
	* The code
	*/
	public void setCode(String code) {
		this.code = code;
	}
	
	/**
	* 
	* @return
	* The display
	*/
	public String getDisplay() {
		return display;
	}
	
	/**
	* 
	* @param display
	* The display
	*/
	public void setDisplay(String display) {
		this.display = display;
	}
}